@router.post("/promotion-suggestions/{suggestion_id}/approve")
async def approve_promotion_suggestion(
    suggestion_id: str,
    body: ApprovalRequest,
    current_user: UserResponse = Depends(get_admin_user),
):
    """
    Approve a promotion suggestion generated by the Customer Experience Agent.

    On approval:
    - Sets suggestion status to 'approved'
    - Writes an active promotion record to the `promotions` collection
      with a 1-day window (start_date = today, end_date = today)
    """
    try:
        db = get_database()
        now = now_ist()
        today_str = now.strftime("%Y-%m-%d")

        result = await db.promotion_suggestions.find_one_and_update(
            {"_id": ObjectId(suggestion_id), "status": "pending"},
            {"$set": {
                "status": "approved",
                "reviewed_at": now,
                "reviewed_by": current_user.email,
                "approval_notes": body.notes,
            }},
            return_document=True,
        )

        if not result:
            return error_response(
                code="NOT_FOUND",
                message="Promotion suggestion not found or already decided",
            )

        # Write an active promotion record so the order service can apply it
        promo_doc = {
            "promo_id": suggestion_id,  # Map for frontend compatibility
            "restaurant_id": result.get("restaurant_id"),
            "suggestion_id": suggestion_id,
            "promo_type": result.get("promo_type"),
            "menu_item_ids_array": result.get("menu_item_ids", []),
            "menu_item_names": result.get("menu_item_names", []),
            "discount_pct": result.get("discount_pct", 0),
            "description": result.get("description", ""),
            "status": "active",
            "start_date": today_str,
            "end_date": today_str,
            "created_at": now,
            "approved_by": current_user.email,
        }
        await db.promotions.insert_one(promo_doc)

        # Notify all staff about the active promotion
        from app.services.orchestrator import get_orchestrator
        await get_orchestrator()._create_notification(
            type="promotion_active",
            title="New promotion live today",
            message=result.get("description", "A new promotion is active today."),
            severity="info",
            target_roles=["admin", "waiter"],
            metadata={"suggestion_id": suggestion_id, "promo_type": result.get("promo_type")},
        )

        return success_response(
            data={"suggestion_id": suggestion_id, "status": "approved"},
            message="Promotion suggestion approved — promotion is now active",
        )

    except Exception as e:
        logger.error(f"Failed to approve promotion suggestion {suggestion_id}: {e}", exc_info=True)
        return error_response(
            code="APPROVAL_FAILED",
            message="Failed to approve promotion suggestion",
            details={"error": str(e)},
        )


