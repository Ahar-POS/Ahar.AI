logger = logging.getLogger(__name__)
router = APIRouter()


# Request models
class ApprovalRequest(BaseModel):
    """Approval or rejection request body"""
    notes: Optional[str] = None


class PartialApprovalRequest(BaseModel):
    """Partial approval request body"""
    material_ids: List[str]
    notes: Optional[str] = None


class POItemDecision(BaseModel):
    """Single per-item decision in a PO review"""
    material_id: str
    action: Literal["approve", "reject"]
    quantity: Optional[float] = Field(None, description="Approved quantity (for approve action)")
    reason: Optional[str] = Field(None, description="Rejection reason (for reject action)")


class POReviewRequest(BaseModel):
    """Option B per-item PO review request"""
    items: List[POItemDecision]


