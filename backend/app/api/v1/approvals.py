@router.post("/{list_id}/approve")
async def approve_shopping_list(
    list_id: str,
    request: ApprovalRequest,
    current_user: UserResponse = Depends(get_current_user)
):
    """
    Approve entire shopping list

    Args:
        list_id: MongoDB ObjectId of shopping list
        request: Approval request with optional notes

    Returns:
        Success confirmation
    """
    if current_user.role != UserRole.ADMIN:
        return error_response(
            code="FORBIDDEN",
            message="Only administrators can approve shopping lists"
        )

    try:
        service = get_shopping_list_service()
        success = await service.approve_list(
            list_id,
            str(current_user.id),
            request.notes
        )

        if not success:
            return error_response(
                code="APPROVAL_FAILED",
                message="Shopping list not found or already processed"
            )

        # Fire Hyperpure order execution in background (non-blocking)
        from app.services.orchestrator import get_orchestrator
        asyncio.create_task(get_orchestrator().execute_approved_order(list_id))

        return success_response(
            data={"approved": True, "list_id": list_id},
            message="Shopping list approved — placing order on Hyperpure"
        )

    except Exception as e:
        logger.error(f"Failed to approve shopping list {list_id}: {e}", exc_info=True)
        return error_response(
            code="APPROVAL_FAILED",
            message="Failed to approve shopping list",
            details={"error": str(e)}
        )


