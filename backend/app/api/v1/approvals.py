class DeliveryConfirmRequest(BaseModel):
    """Staff confirms delivery of ordered items."""
    material_ids: List[str] = Field(..., description="material_ids that were received")
    notes: Optional[str] = None


@router.post("/{list_id}/confirm-delivery")
async def confirm_delivery(
    list_id: str,
    request: DeliveryConfirmRequest,
    current_user: UserResponse = Depends(get_current_user),
):
    """
    Staff confirms receipt of delivered items.

    Marks the items as 'delivered' in the shopping list and updates
    inventory quantities via the inventory service.
    """
    try:
        service = get_shopping_list_service()
        shopping_list = await service.get_shopping_list(list_id)
        if not shopping_list:
            return error_response(code="NOT_FOUND", message="Shopping list not found")

        # Mark items delivered in shopping list
        await service.mark_items_delivered(list_id, request.material_ids)

        # Update inventory quantities for delivered items
        from app.services.inventory_service import get_inventory_service
        inv_service = get_inventory_service()

        items_by_id = {i["material_id"]: i for i in shopping_list.get("items", [])}
        updated_materials = []

        for material_id in request.material_ids:
            item = items_by_id.get(material_id)
            if not item:
                continue
            qty = item.get("approved_quantity") or item.get("quantity_to_order", 0)
            if qty:
                await inv_service.add_stock(material_id, qty, source="hyperpure_delivery")
                updated_materials.append(material_id)

        logger.info(
            f"Delivery confirmed for list {list_id}: "
            f"{len(updated_materials)} items, user={current_user.email}"
        )

        return success_response(
            data={
                "list_id": list_id,
                "delivered_items": len(updated_materials),
                "material_ids": updated_materials,
            },
            message=f"Delivery confirmed — {len(updated_materials)} item(s) added to inventory",
        )

    except Exception as e:
        logger.error(f"Failed to confirm delivery for list {list_id}: {e}", exc_info=True)
        return error_response(
            code="DELIVERY_FAILED",
            message="Failed to confirm delivery",
            details={"error": str(e)},
        )
