@router.post("/purchase-orders/{po_id}/review")
async def review_purchase_order(
    po_id: str,
    request: POReviewRequest,
    current_user: UserResponse = Depends(get_admin_user)
):
    """
    Apply per-item approve/reject decisions to a purchase order (Option B).

    Partial submissions are valid — undecided items remain pending.
    Owner can call this multiple times until all items have a decision.

    Args:
        po_id: MongoDB ObjectId of the shopping list (PO)
        request: List of per-item decisions

    Returns:
        Summary with approved/rejected/pending counts and approved total
    """
    try:
        service = get_shopping_list_service()
        item_decisions = [d.model_dump() for d in request.items]

        result = await service.review_po_items(
            po_id,
            item_decisions,
            str(current_user.id)
        )

        if result is None:
            return error_response(
                code="NOT_FOUND",
                message="Purchase order not found"
            )

        # Fire Hyperpure order execution in background (non-blocking)
        # This will pick up any items newly marked as 'approved'
        from app.services.orchestrator import get_orchestrator
        asyncio.create_task(get_orchestrator().execute_approved_order(po_id))

        return success_response(
            data={
                "po_id": po_id,
                "status": result["list_status"],
                "approved_items": result["approved"],
                "rejected_items": result["rejected"],
                "pending_items": result["pending"],
                "approved_total_paise": result["approved_total_paise"],
            },
            message="Purchase order decisions recorded"
        )

    except ValueError as e:
        return error_response(
            code="INVALID_STATE",
            message=str(e)
        )
    except Exception as e:
        logger.error(f"Failed to review PO {po_id}: {e}", exc_info=True)
        return error_response(
            code="REVIEW_FAILED",
            message="Failed to record PO review decisions",
            details={"error": str(e)}
        )


