@router.get("/hyperpure-orders")
async def get_hyperpure_orders(
    status: Optional[str] = Query(None, description="Filter by status: pending, fully_received"),
    current_user: UserResponse = Depends(get_current_user)
):
    """
    Return all purchase orders placed via Hyperpure (source=hyperpure).
    Read-only — used by the Hyperpure Orders tracking page.
    """
    try:
        from app.repositories.purchase_order_repository import get_purchase_order_repository
        repo = get_purchase_order_repository()
        collection = repo._get_collection()

        query: dict = {"source": "hyperpure"}
        if status:
            query["status"] = status

        cursor = collection.find(query).sort("created_at", -1).limit(200)
        orders = await cursor.to_list(length=200)
        for o in orders:
            o["_id"] = str(o["_id"])
            for field in ("ordered_at", "delivered_at", "created_at", "updated_at"):
                if field in o and hasattr(o[field], "isoformat"):
                    o[field] = o[field].isoformat()

        return success_response(data=orders, message=f"Found {len(orders)} Hyperpure orders")

    except Exception as e:
        logger.error(f"Failed to fetch Hyperpure orders: {e}", exc_info=True)
        return error_response(
            code="FETCH_FAILED",
            message="Failed to fetch Hyperpure orders",
            details={"error": str(e)},
        )


